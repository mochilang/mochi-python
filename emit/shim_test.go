func TestEmitShimMetadata(t *testing.T) {
	w := &wrapper.Wrapper{Package: "httpx", Module: "httpx_externs"}
	s, err := EmitShim(w, DefaultOptions())
	if err != nil {
		t.Fatalf("err: %v", err)
	}
	if s.Package != "httpx" || s.Module != "httpx_shim" {
		t.Errorf("metadata: %+v", s)
	}
	if s.SHA256 == "" {
		t.Error("SHA256 missing")
	}
	sum := sha256.Sum256([]byte(s.Source))
	if hex.EncodeToString(sum[:]) != s.SHA256 {
		t.Errorf("SHA256 does not match Source")
	}
}

func TestEmitShimHeader(t *testing.T) {
	w := &wrapper.Wrapper{Package: "httpx"}
	s, _ := EmitShim(w, DefaultOptions())
	for _, head := range []string{
		"// Auto-generated by mochi pkg lock",
		"// MEP-71 Mochi shim for source package: httpx",
		"// Items: 0  Skipped: 0  Loop: per-call",
	} {
		if !strings.Contains(s.Source, head) {
			t.Errorf("missing header %q\n--- got ---\n%s", head, s.Source)
		}
	}
}

