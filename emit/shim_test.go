func TestEmitShimGroupingOrder(t *testing.T) {
	// One of each kind; ensure they appear in the documented group order:
	// records, interfaces, constants, functions.
	rec := typemap.MochiType{
		Kind:   typemap.KindRecord,
		Name:   "Rec",
		Fields: []typemap.MochiField{{Name: "x", Type: typemap.NewScalar("int")}},
	}
	iface := typemap.MochiType{Kind: typemap.KindInterface, Name: "If"}
	fn := typemap.NewFun(typemap.NewScalar("int"))
	w := &wrapper.Wrapper{
		Package: "p",
		Items: []wrapper.Item{
			{Name: "fn", SourceName: "p.fn", Kind: wrapper.ItemFunc, Type: fn},
			{Name: "If", SourceName: "p.If", Kind: wrapper.ItemInterface, Type: iface},
			{Name: "VER", SourceName: "p.VER", Kind: wrapper.ItemConstant, Type: typemap.NewScalar("string")},
			{Name: "Rec", SourceName: "p.Rec", Kind: wrapper.ItemRecord, Type: rec},
		},
	}
	s, _ := EmitShim(w, DefaultOptions())
	idxRec := strings.Index(s.Source, "type Rec =")
	idxIf := strings.Index(s.Source, "extern python type If")
	idxVar := strings.Index(s.Source, "extern python var VER")
	idxFn := strings.Index(s.Source, "extern python fun fn")
	if !(idxRec < idxIf && idxIf < idxVar && idxVar < idxFn) {
		t.Errorf("group order violated: rec=%d iface=%d var=%d fn=%d\n%s",
			idxRec, idxIf, idxVar, idxFn, s.Source)
	}
}

func TestEmitShimStableSourceNameOrder(t *testing.T) {
	w := &wrapper.Wrapper{
		Package: "p",
		Items: []wrapper.Item{
			{Name: "zeta", SourceName: "p.zeta", Kind: wrapper.ItemFunc, Type: typemap.NewFun(typemap.NewScalar("int"))},
			{Name: "alpha", SourceName: "p.alpha", Kind: wrapper.ItemFunc, Type: typemap.NewFun(typemap.NewScalar("int"))},
			{Name: "mu", SourceName: "p.mu", Kind: wrapper.ItemFunc, Type: typemap.NewFun(typemap.NewScalar("int"))},
		},
	}
	s, _ := EmitShim(w, DefaultOptions())
	a := strings.Index(s.Source, "extern python fun alpha")
	m := strings.Index(s.Source, "extern python fun mu")
	z := strings.Index(s.Source, "extern python fun zeta")
	if !(a < m && m < z) {
		t.Errorf("not sorted: alpha=%d mu=%d zeta=%d\n%s", a, m, z, s.Source)
	}
}

