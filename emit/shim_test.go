func TestEmitShimFunction(t *testing.T) {
	w := &wrapper.Wrapper{
		Package: "math2",
		Items: []wrapper.Item{
			{
				Name:       "add",
				SourceName: "math2.add",
				Kind:       wrapper.ItemFunc,
				Type: typemap.NewFun(
					typemap.NewScalar("int"),
					typemap.NewScalar("int"),
					typemap.NewScalar("int"),
				),
			},
		},
	}
	s, _ := EmitShim(w, DefaultOptions())
	if !strings.Contains(s.Source, "extern python fun add(int, int): int") {
		t.Errorf("missing extern fun: %s", s.Source)
	}
	if len(s.Decls) != 1 || s.Decls[0].Kind != DeclExternFun {
		t.Errorf("decls = %+v", s.Decls)
	}
}

