package build

import (
	"fmt"
	"os"
	"path/filepath"
	"strings"
)

// Phase 18.0: emit a `mochi build --target=python-publish` output that
// bundles the Phase 15 dist artefacts (wheel + sdist) plus a Trusted
// Publishing GitHub Actions workflow under `.github/workflows/`.
//
// Layout:
//
//	<outDir>/
//	  dist/<pkg>-<ver>-py3-none-any.whl
//	  dist/<pkg>-<ver>.tar.gz
//	  .github/workflows/publish.yml
//	  PUBLISHING.md
//
// The workflow is structured per the PyPI Trusted Publishing docs at
// https://docs.pypi.org/trusted-publishers/. Three jobs:
//
//	- build: rebuild the dist artefacts reproducibly under CI
//	  (SOURCE_DATE_EPOCH = git commit ts) and upload them as an
//	  artifact.
//	- publish: triggered on `release: published`. Downloads the
//	  artifact and runs `uv publish --trusted-publishing always`,
//	  exchanging the GitHub Actions OIDC token for a 15-minute PyPI
//	  upload credential. No long-lived `PYPI_API_TOKEN` secret in the
//	  workflow; the only thing the maintainer registers on PyPI is the
//	  Trusted Publisher entry (repo + workflow filename + environment).
//	- publish-dryrun: triggered on PRs that touch the workflow or
//	  pyproject.toml. Same OIDC dance, but uses `uv publish --dry-run`
//	  against TestPyPI so failed token configuration surfaces in the
//	  PR gate before a tagged release tries the real upload.
//
// The end-to-end OIDC + sigstore round-trip is exercised by the CI
// publish-dryrun job, not by `go test`: it depends on a TestPyPI
// account and a Trusted Publisher entry that only PyPI can validate.
// The Go-side phase 18 gate verifies the workflow YAML shape (every
// claim the doc makes about the workflow is structurally checked) so
// the published workflow does not silently drift away from the spec.

// buildPublishWorkflow emits the Phase 18 publish bundle under outDir.
// Returns the path to the workflow YAML.
func buildPublishWorkflow(outDir, workDir, rtDir, pkgName string) (string, error) {
	distDir := filepath.Join(outDir, "dist")
	if err := os.MkdirAll(distDir, 0o755); err != nil {
		return "", err
	}
	whl, err := buildWheel(distDir, workDir, rtDir, pkgName)
	if err != nil {
		return "", fmt.Errorf("wheel: %w", err)
	}
	sdist, err := buildSdist(distDir, workDir, rtDir, pkgName)
	if err != nil {
		return "", fmt.Errorf("sdist: %w", err)
	}
	_ = whl
	_ = sdist

	wfDir := filepath.Join(outDir, ".github", "workflows")
	if err := os.MkdirAll(wfDir, 0o755); err != nil {
		return "", err
	}
	wfPath := filepath.Join(wfDir, "publish.yml")
	yaml := renderPublishWorkflow(pkgName)
	if err := os.WriteFile(wfPath, []byte(yaml), 0o644); err != nil {
		return "", err
	}

	guide := renderPublishingGuide(pkgName)
	if err := os.WriteFile(filepath.Join(outDir, "PUBLISHING.md"), []byte(guide), 0o644); err != nil {
		return "", err
	}

	return wfPath, nil
}

// renderPublishWorkflow returns the publish.yml workflow body. The
// filename is significant: PyPI's Trusted Publisher trust binds the
// workflow filename, so renaming this file breaks the OIDC match. The
// generated guide tells the maintainer to keep the filename in sync
// with the PyPI-side config.
func renderPublishWorkflow(pkgName string) string {
	const tpl = `# Auto-generated by mochi build --target=python-publish.
# Filename is part of the PyPI Trusted Publisher trust (repo +
# workflow filename + environment); do not rename without updating
# the PyPI Trusted Publisher config and the TestPyPI config.
name: Publish %s to PyPI

on:
  release:
    types: [published]
  workflow_dispatch:
    inputs:
      dry_run:
        description: "Dry-run against TestPyPI only"
        required: false
        default: "true"
        type: string
  pull_request:
    paths:
      - .github/workflows/publish.yml
      - pyproject.toml

jobs:
  build:
    name: Build wheel + sdist
    runs-on: ubuntu-24.04
    steps:
      - uses: actions/checkout@v4
        with:
          fetch-depth: 0
      - uses: astral-sh/setup-uv@v3
        with:
          version: "0.7.0"
          enable-cache: true
      - run: uv python install 3.12.7
      - name: Build reproducibly
        run: |
          export SOURCE_DATE_EPOCH=$(git log -1 --pretty=%%ct)
          uv build
      - uses: actions/upload-artifact@v4
        with:
          name: dist
          path: dist/
          if-no-files-found: error

  publish:
    name: Publish via Trusted Publishing
    needs: build
    runs-on: ubuntu-24.04
    if: github.event_name == 'release'
    environment: pypi
    permissions:
      id-token: write
      contents: read
      attestations: write
    steps:
      - uses: actions/download-artifact@v4
        with:
          name: dist
          path: dist/
      - uses: astral-sh/setup-uv@v3
        with:
          version: "0.7.0"
      - name: Publish to PyPI
        run: uv publish --trusted-publishing always dist/*

  publish-dryrun:
    name: TestPyPI dry-run
    needs: build
    runs-on: ubuntu-24.04
    if: github.event_name == 'pull_request' || (github.event_name == 'workflow_dispatch' && inputs.dry_run == 'true')
    environment: testpypi
    permissions:
      id-token: write
      contents: read
    steps:
      - uses: actions/download-artifact@v4
        with:
          name: dist
          path: dist/
      - uses: astral-sh/setup-uv@v3
        with:
          version: "0.7.0"
      - name: Dry-run publish to TestPyPI
        run: |
          uv publish --dry-run --trusted-publishing always \
            --publish-url https://test.pypi.org/legacy/ \
            dist/*
`
	return fmt.Sprintf(tpl, pkgName)
}

// renderPublishingGuide is the human-facing checklist that ships in
// outDir/PUBLISHING.md. It walks the maintainer through the one-time
// PyPI and TestPyPI Trusted Publisher setup so the workflow runs
// without an API token.
func renderPublishingGuide(pkgName string) string {
	var b strings.Builder
	fmt.Fprintf(&b, "# Publishing %s\n\n", pkgName)
	b.WriteString("This bundle was emitted by `mochi build --target=python-publish`. The wheel and sdist are under `dist/`; the publish workflow is at `.github/workflows/publish.yml`.\n\n")
	b.WriteString("## One-time setup\n\n")
	b.WriteString("1. Reserve the PyPI project name at https://pypi.org/manage/account/publishing/ (Add a new pending publisher).\n")
	b.WriteString(fmt.Sprintf("   - PyPI Project Name: `%s`\n", pkgName))
	b.WriteString("   - Owner: `<your-github-org>`\n")
	b.WriteString("   - Repository name: `<repo-with-publish-workflow>`\n")
	b.WriteString("   - Workflow name: `publish.yml`\n")
	b.WriteString("   - Environment name: `pypi`\n")
	b.WriteString("2. Repeat at https://test.pypi.org/ with environment name `testpypi` for the PR dry-run gate.\n")
	b.WriteString("3. In the GitHub repo, create two environments under Settings > Environments: `pypi` (production, optionally require manual approval) and `testpypi` (no approval).\n\n")
	b.WriteString("## Cutting a release\n\n")
	b.WriteString("1. Bump `[project].version` in the source `.mochi` package metadata (Mochi will regenerate this `pyproject.toml`).\n")
	b.WriteString("2. Run `mochi build --target=python-publish --out dist-publish/` locally to rebuild the artefacts.\n")
	b.WriteString("3. Tag the commit and create a GitHub Release. The `publish` job runs automatically and uploads the wheel + sdist via OIDC.\n\n")
	b.WriteString("## Why no `PYPI_API_TOKEN`?\n\n")
	b.WriteString("The workflow uses PyPI Trusted Publishing (OIDC token exchange via GitHub Actions). A long-lived API token in `secrets.PYPI_API_TOKEN` is a supply-chain risk; the 2022-2024 PyPI typosquat incidents motivated PyPI to recommend OIDC for every project. The emitted workflow refuses to fall back to token auth.\n\n")
	b.WriteString("## Verifying a published wheel\n\n")
	b.WriteString("Each upload includes a sigstore-signed PEP 740 attestation. To verify after publish:\n\n")
	b.WriteString("```\n")
	b.WriteString(fmt.Sprintf("pip install sigstore\nsigstore verify pypi %s\n", pkgName))
	b.WriteString("```\n")
	return b.String()
}
