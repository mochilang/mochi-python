package build

import (
	"os"
	"path/filepath"
	"strings"
	"testing"
)

// TestPhase0Skeleton is the per-phase sentinel test. The CI gate for phase 0
// LANDED requires this test to pass on every supported host.
//
// Sub-tests:
//   - end_to_end: Driver allocates work-dir, writes venv root, re-reads it,
//     the manifest parses to expected content, cleanup removes the work-dir.
//   - package_layout: the package3/python/ tree exists at the expected paths
//     and has the documented Go packages.
//   - default_venv_invariants: DefaultVenv passes Validate and renders the
//     auto-generated header plus the [project] and [python] tables.
func TestPhase0Skeleton(t *testing.T) {
	t.Run("end_to_end", func(t *testing.T) {
		d := NewDriver(Options{NoCache: true})
		defer d.Cleanup()

		v, err := d.PrepareVenv()
		if err != nil {
			t.Fatalf("PrepareVenv: %v", err)
		}
		v.AddMember(VenvMember{Name: "mochi_user", Path: "mochi_user", Kind: MemberUser})
		v.AddMember(VenvMember{Name: "httpx", Path: "python_wrap/httpx", Kind: MemberWrapper})
		v.AddSharedDep("httpx", "==0.27.0")

		root, err := d.WriteVenvRoot(v)
		if err != nil {
			t.Fatalf("WriteVenvRoot: %v", err)
		}
		manifestBytes, err := os.ReadFile(filepath.Join(root, "pyproject.toml"))
		if err != nil {
			t.Fatalf("read pyproject.toml: %v", err)
		}
		manifest := string(manifestBytes)
		wantBits := []string{
			"[build-system]",
			"[project]",
			`name = "mochi-app"`,
			`requires-python = ">=3.12,<3.15"`,
			`    "httpx==0.27.0",`,
			"[python]",
			`implementation = "cpython"`,
			`runtime-mode = "embedded"`,
			`async-mode = "per-call"`,
			"free-threaded = false",
			"[python.wrappers]",
			`"python_wrap/httpx" = { name = "httpx", kind = "wrapper" }`,
			"[python.members]",
			`"mochi_user" = { name = "mochi_user", kind = "user" }`,
		}
		for _, bit := range wantBits {
			if !strings.Contains(manifest, bit) {
				t.Errorf("manifest missing %q\n--- output ---\n%s", bit, manifest)
			}
		}

		work := d.WorkDir()
		if err := d.Cleanup(); err != nil {
			t.Fatalf("Cleanup: %v", err)
		}
		if _, err := os.Stat(work); !os.IsNotExist(err) {
			t.Errorf("work-dir not removed by Cleanup: %v", err)
		}
	})

	t.Run("package_layout", func(t *testing.T) {
		// Locate package3/python/ relative to this test source file. The test
		// runs from package3/python/build/, so the package root is one
		// directory up.
		here, err := os.Getwd()
		if err != nil {
			t.Fatalf("Getwd: %v", err)
		}
		pkgRoot := filepath.Dir(here)
		expected := []string{
			"README.md",
			"build/driver.go",
			"build/venv.go",
			"errors/errors.go",
		}
		for _, rel := range expected {
			path := filepath.Join(pkgRoot, rel)
			info, err := os.Stat(path)
			if err != nil {
				t.Errorf("expected file %s missing: %v", rel, err)
				continue
			}
			if info.IsDir() {
				t.Errorf("expected file %s is a directory", rel)
			}
		}
	})

	t.Run("default_venv_invariants", func(t *testing.T) {
		v := DefaultVenv()
		if err := v.Validate(); err != nil {
			t.Errorf("DefaultVenv().Validate() = %v; want nil", err)
		}
		manifest := v.RenderPyprojectToml()
		if !strings.Contains(manifest, "Auto-generated by MEP-71") {
			t.Errorf("DefaultVenv render missing header")
		}
		if !strings.Contains(manifest, "[project]") {
			t.Errorf("DefaultVenv render missing [project] table")
		}
		if !strings.Contains(manifest, "[python]") {
			t.Errorf("DefaultVenv render missing [python] table")
		}
		if !strings.Contains(manifest, "[build-system]") {
			t.Errorf("DefaultVenv render missing [build-system] table")
		}
	})
}
