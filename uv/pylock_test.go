package uv

import (
	"strings"
	"testing"
)

const samplePyLock = `lock-version = "1.0"
created-by = "uv"
requires-python = ">=3.10"

[[packages]]
name = "httpx"
version = "0.27.0"
requires-python = ">=3.8"
dependencies = ["anyio", "certifi"]

[[packages.wheels]]
name = "httpx-0.27.0-py3-none-any.whl"
url = "https://files.pythonhosted.org/packages/abc/httpx-0.27.0-py3-none-any.whl"
hashes = {sha256 = "cafebabe"}

[packages.sdist]
name = "httpx-0.27.0.tar.gz"
url = "https://files.pythonhosted.org/packages/xyz/httpx-0.27.0.tar.gz"
hashes = {sha256 = "deadbeef"}

[[packages]]
name = "anyio"
version = "4.3.0"
marker = "python_version >= '3.10'"

[[packages.wheels]]
name = "anyio-4.3.0-py3-none-any.whl"
url = "https://example.com/anyio-4.3.0-py3-none-any.whl"
hashes = {sha256 = "feedface", blake3 = "abc123"}
`

func TestParsePyLock(t *testing.T) {
	pl, err := ParsePyLock([]byte(samplePyLock))
	if err != nil {
		t.Fatalf("ParsePyLock err = %v", err)
	}
	if pl.LockVersion != "1.0" {
		t.Errorf("LockVersion = %q", pl.LockVersion)
	}
	if pl.CreatedBy != "uv" {
		t.Errorf("CreatedBy = %q", pl.CreatedBy)
	}
	if pl.RequiresPython != ">=3.10" {
		t.Errorf("RequiresPython = %q", pl.RequiresPython)
	}
	if len(pl.Packages) != 2 {
		t.Fatalf("len(Packages) = %d", len(pl.Packages))
	}
	httpx := pl.Packages[0]
	if httpx.Name != "httpx" || httpx.Version != "0.27.0" {
		t.Errorf("httpx = %+v", httpx)
	}
	if len(httpx.Dependencies) != 2 {
		t.Errorf("httpx deps = %v", httpx.Dependencies)
	}
	if len(httpx.Wheels) != 1 || httpx.Wheels[0].Hashes["sha256"] != "cafebabe" {
		t.Errorf("httpx wheels = %+v", httpx.Wheels)
	}
	if httpx.Sdist == nil || httpx.Sdist.Hashes["sha256"] != "deadbeef" {
		t.Errorf("httpx sdist = %+v", httpx.Sdist)
	}

	anyio := pl.Packages[1]
	if anyio.Marker == "" {
		t.Errorf("anyio marker = %q; want non-empty", anyio.Marker)
	}
	if anyio.Wheels[0].Hashes["blake3"] != "abc123" {
		t.Errorf("anyio blake3 = %q", anyio.Wheels[0].Hashes["blake3"])
	}
}

